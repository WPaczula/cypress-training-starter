// ================================== POSTAW TO SAM =========================================

// Po pierwsze stwórz nowy folder i zainicjuj npma:
`npm init`

// Drugim krokiem będzie instalowanie cypressa
`npm i cypress`

// I odpalenie w trybie open, aby zainicjalizowac projekt
`npx cypress open`

// Kolejne razy mozna odpalać w trybie headless
`npx cypress run`